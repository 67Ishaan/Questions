#include<bits/stdc++.h>
using namespace std;
int main()
{
  float radius, area, circum;
    cout<<"Enter Radius of Circle : ";
    cin>>radius;
    area=3.14*radius*radius;
    cout<<"The area of Circle is : "<<area<<"\n";

    circum=2*3.14*radius;
    cout<<"The Circumference of Circle is : "<<circum<<"\n";
    return 0;
}
